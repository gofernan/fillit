#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*d;

	d = (char *)s;
	i = 0;
	while (d[i] != 0)
	{
		if (d[i] == c)
			return (&d[i]);
		i++;
	}
	if (d[i] == 0 && c == 0)
		return (&d[i]);
	return (NULL);
}
